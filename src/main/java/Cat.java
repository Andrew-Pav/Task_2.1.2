public class Cat {
    private double speed;
    private String name;

    public Cat() {

    }

    public Cat(double speed, String name) {
        this.speed = speed;
        this.name = name;
    }

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
